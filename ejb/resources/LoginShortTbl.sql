DROP TABLE USERS;


CREATE TABLE USERS (
  USER_ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),
  FULLNAME VARCHAR(255),
	EMAIL VARCHAR(255),
	PASSWORD VARCHAR(255),
  LASTLOGDATE TIMESTAMP,
  PRIMARY KEY (USER_ID)
);

