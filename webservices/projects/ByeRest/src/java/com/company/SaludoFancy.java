package com.company;

import java.util.Date;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author fredfred2
 */
@XmlRootElement
public class SaludoFancy {

    private String name;
    private Date date;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }
    
    
}
